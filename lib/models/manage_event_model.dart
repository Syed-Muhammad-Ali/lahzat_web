
class UserItem {
  final int sl;
  final String name;
  final String phone;
  final String avatar;
  final String date;
  final String category;
  final String paymentStatus;
  final String eventStatus;

  UserItem({
    required this.sl,
    required this.name,
    required this.phone,
    required this.avatar,
    required this.date,
    required this.category,
    required this.paymentStatus,
    required this.eventStatus,
  });
}
